def main():
    # Countdown from 10 to 1
    for i in range(10, 0, -1):
        print(i, end=" ")  # Print numbers on the same line with a space in between
    print("Liftoff!")  # Print 'Liftoff!' after the countdown

# This provided line is required at the end
# of the Python file to call the main() function.
if __name__ == '__main__':
    main()


# Ji bilkul! Main aapko step-by-step samjhaata hoon.

# for loop ka use: Hum for loop ka istemal kar rahe hain taake 10 se lekar 1 tak countdown kar sakein. range(10, 0, -1) ka matlab hai:

# 10: Loop 10 se start karega.

# 0: Loop 0 tak nahi jaayega, isliye 1 tak count karega.

# -1: Har baar 1 ka step decrease hoga (i.e., 10, 9, 8, ..., 1).

# print(i, end=" "): Yeh line har number ko print karti hai aur end=" " ka matlab hai ke har number ke baad space lagayega, taake sab numbers ek hi line mein dikhein.

# print("Liftoff!"): Jab loop khatam ho jata hai (i.e., jab 1 print ho jata hai), tab hum "Liftoff!" print karte hain.

